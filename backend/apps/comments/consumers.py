"""
# Модуль для работы с асинхронными WebSocket-соединениями.
# WebSocket – это постоянное двустороннее соединение между браузером и сервером.
# В отличие от обычного HTTP (запрос → ответ → соединение закрыто),
# WebSocket остаётся открытым: сервер может сам отправлять данные браузеру
# в любой момент без запроса со стороны клиента.
"""
import json

from channels.generic.websocket import AsyncWebsocketConsumer


class CommentConsumer(AsyncWebsocketConsumer):
    """
    WebSocket-потребитель (consumer) для обновления комментариев в реальном времени.

    Принцип работы:
    1. Браузер открывает WebSocket-соединение с сервером
    2. Consumer добавляет это соединение в группу 'comments'
    3. Когда кто-то создаёт новый комментарий через REST API,
       сервер рассылает уведомление всем в группе 'comments'
    4. Браузер получает уведомление и обновляет список комментариев
    """

    # Название группы каналов — все подключённые браузеры объединяются
    # в эту группу и получают одинаковые сообщения (broadcast)
    room_group_name = 'comments'

    async def connect(self):
        """
        Вызывается когда браузер устанавливает WebSocket-соединение.
        Добавляем текущее соединение в группу 'comments',
        чтобы оно получало все сообщения группы.
        """
        # Добавляем текущий канал (соединение) в группу 'comments'.
        # self.channel_name — уникальное имя этого конкретного соединения,
        # присваивается автоматически фреймворком Channels
        await self.channel_layer.group_add(
            self.room_group_name,  # название группы
            self.channel_name      # уникальное имя этого соединения
        )

        # Подтверждаем соединение — без этого браузер получит ошибку 403
        await self.accept()

    async def disconnect(self, close_code):
        """
        Вызывается когда браузер закрывает WebSocket-соединение
        (закрыл вкладку, перешёл на другую страницу и т.д.).
        Удаляем соединение из группы, чтобы не отправлять
        сообщения закрытому соединению.

        close_code — числовой код причины закрытия (например, 1000 — нормальное закрытие)
        """
        await self.channel_layer.group_discard(
            self.room_group_name,  # название группы
            self.channel_name      # удаляем именно это соединение
        )

    async def receive(self, text_data):
        """
        Вызывается когда браузер отправляет сообщение через WebSocket.
        В нашем проекте браузер не отправляет данные через WebSocket —
        для создания комментариев используется REST API (POST /api/comments/).
        Этот метод оставлен для полноты реализации.

        text_data — строка JSON, полученная от браузера
        """
        pass  # Ничего не делаем — только слушаем

    async def new_comment(self, event):
        """
        Вызывается когда в группу 'comments' приходит сообщение
        с типом 'new_comment'. Это происходит из views.py после
        успешного создания нового комментария через REST API.

        event — словарь с данными события:
            {
                'type': 'new_comment',
                'comment': { id, user_name, text, ... }
            }

        Метод рассылает данные нового комментария всем браузерам,
        подключённым к WebSocket в данный момент.
        """
        # Извлекаем данные комментария из события
        comment_data = event['comment']

        # Отправляем данные комментария браузеру в формате JSON
        await self.send(text_data=json.dumps({
            'type': 'new_comment',
            'comment': comment_data
        }))

"""
Как это работает в проекте
```
Пользователь А добавляет комментарий (POST /api/comments/)
        ↓
views.py хранит комментарий в БД
        ↓
views.py отправляет сообщения в группу 'comments'
        ↓
CommentConsumer.new_comment() срабатывает у всех подключенных
        ↓
Браузеры пользователей Б, В, Г получают новый комментарий
без перезагрузки страницы
"""