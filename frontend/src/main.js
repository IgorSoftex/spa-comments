// Точка входа Vue-приложения.
// Это первый файл который выполняется при загрузке страницы.
// Создаём экземпляр Vue и монтируем его в <div id="app"> из index.html.

// createApp — функция для создания экземпляра Vue-приложения
import { createApp } from 'vue'

// Корневой компонент — отправная точка дерева компонентов
import App from './App.vue'

// Создаём приложение и сразу монтируем в DOM.
// '#app' — это <div id="app"> в index.html.
// После монтирования Vue берёт контроль над этим элементом
// и отображает компоненты внутри него.
createApp(App).mount('#app')
