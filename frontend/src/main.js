// Точка входа Vue-приложения.
// Это первый файл который выполняется при загрузке страницы.
// Здесь мы создаём Vue-приложение, подключаем плагины и монтируем его в HTML.

// createApp — функция для создания экземпляра Vue-приложения
import { createApp } from 'vue'

// createPinia — функция для создания хранилища состояния (Pinia).
// Pinia — это официальный менеджер состояния для Vue 3.
// Хранит данные (комментарии, состояние загрузки) доступные
// из любого компонента без передачи через props.
import { createPinia } from 'pinia'

// Корневой компонент приложения — отправная точка дерева компонентов
import App from './App.vue'

// Создаём экземпляр Vue-приложения на основе корневого компонента App
const app = createApp(App)

// Подключаем Pinia как плагин — теперь все компоненты
// могут использовать хранилище через useCommentsStore()
app.use(createPinia())

// Монтируем приложение в DOM-элемент с id="app".
// Это тот самый <div id="app"> из index.html.
// После этого Vue берёт контроль над этим элементом
// и отображает компоненты внутри него.
app.mount('#app')
